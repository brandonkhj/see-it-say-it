          What is near-miss?
          <ul style="list-style: disc;margin-left:10px;">
            <li>Slips, trips, and falls: Near-miss incidents related to slips, trips, and falls are common in industrial settings. For instance, if an employee slips on a wet floor but manages to catch themselves before falling or colliding with a nearby object, it would be considered a near miss.</li>
            <li>Chemical spills or leaks: Industrial facilities often deal with hazardous chemicals. A near-miss could involve a chemical spill or leak that was promptly contained and cleaned up before any harm occurred. These incidents highlight the importance of proper storage, handling, and spill response procedures.</li>
            <li>Fire or explosion hazards: Near-miss incidents related to fire or explosion hazards can be extremely dangerous. For example, a spark near flammable materials that is quickly identified and extinguished before a fire ignites would be considered a near miss.</li>
            <li>Crane or lifting equipment incidents: Industrial sites frequently utilize cranes and lifting equipment for various tasks. A near-miss incident could involve an incident where a load starts to fall but is quickly secured or a crane operator narrowly avoids colliding with another structure or vehicle.</li>
          </ul>
        </li>
        <br/>
        <li>
          What is property damage?
          <ul style="list-style: disc;margin-left:10px;">
            <li>Structural damage: This involves damage to buildings, facilities, or structures within the industrial site. For instance, a collapsed roof, damaged walls, or compromised foundations due to natural disasters, equipment failures, or other incidents.</li>
            <li>Water damage: Water damage can result from various sources, such as burst pipes, plumbing failures, or flooding. It can lead to structural damage, electrical system malfunction, corrosion of equipment, and deterioration of stored materials.</li>
            <li>Vandalism or sabotage: Acts of vandalism or sabotage can cause deliberate property damage within industrial sites. This can include destruction of equipment, defacement of structures, or disruption of critical systems, leading to production delays and financial losses.</li>
          </ul>
        </li>
        <br/>
        <li>
          What is unsafe act/attitude?
          <ul style="list-style: disc;margin-left:10px;">
            <li>Not following safety rules.</li>
            <li>Not wearing PPEs/safety appliances.</li>
            <li>Overfeeding & speeding the machines/vehicle.</li>
            <li>Smoking in prohibited area, ignorance of safety risk/hazard.</li>
            <li>Performing unauthorized work/operation.</li>
            <li>Working on dangerous equipment.</li>
            <li>Using defective tools.</li>
            <li>Unsafe position:
              <ul style="list-style: disc;margin-left:10px;">
                <li>Lifting with back bent.</li>
                <li>Walking or standing under suspended load.</li>
              </ul>
            </li>
          </ul>
        </li>
        <br/>
        <li>
          What is unsafe condition/equipment?
          <ul style="list-style: disc;margin-left:10px;">
            <li>Uncovered hole or drain or pits.</li>
            <li>Fragile roof or damaged ladders.</li>
            <li>Blocking and obstructing pathways and gangways.</li>
            <li>Contaminated floor - Oil/coolant leakage or spillage on the floor.</li>
            <li>Metal chips scattered around the machine/on the floor.</li>
            <li>Rotating machine parts without safety guard or defective guard or inadequately guarded.</li>
            <li>Improper/Low lux level (illumination).</li>
            <li>Improper ventilation at the workplace - not enough air, contamination of air.</li>
            <li>Unwanted items/tools lying on the machine (poor 5S at the workplace).</li>
            <li>Unsafe unloading - lifting too heavy loads, overloading.</li>
            <li>Loose wiring on the machine or at the workplace can lead to electric fire or electric shocks.</li>
            <li>Equipment defects - sharp edges, poorly designed, not strong enough, worn out, cracked, etc.</li>
          </ul>
        </li>
        <br/>
        <li>
          What is the difference between unsafe act and unsafe condition?
          <ul style="list-style: disc;margin-left:10px;">
            <li>Unsafe acts can be defined as any activity by workers that is not in accordance with the established safety standard or practice and that can or is likely to create accidents, risk to oneself or others at work, damage equipment, and cause reputational and financial losses to the employer.</li>
            <li>Unsafe conditions are the risks/hazards present at the workplace that may lead to human injury or death.</li>
          </ul>
        </li>
      </ol>
